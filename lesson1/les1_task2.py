# Выполнить логические побитовые операции «И», «ИЛИ» и др. над числами 5 и 6.
# Выполнить над числом 5 побитовый сдвиг вправо и влево на два знака.

print(f'Логическая операция "И" над числами 5 и 6: {5 & 6}')
print(f'Логическая операция "ИЛИ" над числами 5 и 6: {5 | 6}')
print(f'Логическая операция "исключающее ИЛИ" над числами 5 и 6: {5 ^ 6}')
print(f'Побитовый сдвиг над числом 5 влево {5 << 2}')
print(f'Побитовый сдвиг над числом 5 вправо {5 >> 2}')
